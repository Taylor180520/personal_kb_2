const categoryIcons = {
  'Recently Used': '🕑',
  'Smileys & People': '😀',
  'Animals & Nature': '🐶',
  'Food & Drink': '🍎',
  'Activities': '⚽',
  'Travel & Places': '🚗',
  'Objects': '💡',
  'Symbols': '❤️',
  'Flags': '🚩'
};

const EditCentralKBModal: React.FC<EditCentralKBModalProps> = ({
  isOpen,
  onClose,
  onSubmit,
  currentName,
  currentEmoji
}) => {
  const [name, setName] = useState(currentName);
  const [selectedEmoji, setSelectedEmoji] = useState(currentEmoji);
  const [searchQuery, setSearchQuery] = useState('');
  const [activeCategory, setActiveCategory] = useState('Recently Used');

  useEffect(() => {
    setName(currentName);
    setSelectedEmoji(currentEmoji);
  }, [currentName, currentEmoji, isOpen]);

  const handleSubmit = (e: React.FormEvent) => {
    e.preventDefault();
    if (name.trim()) {
      onSubmit(name.trim(), selectedEmoji);
      onClose();
    }
  };

  const filteredEmojis = searchQuery
    ? Object.values(emojiData).flat().filter(emoji => {
        // In a real app, you'd have emoji names/keywords to search through
        return emoji.includes(searchQuery);
      })
    : emojiData[activeCategory as keyof typeof emojiData] || [];

  const handleEmojiSelect = (emoji: string) => {
    setSelectedEmoji(emoji);
  };

  if (!isOpen) return null;

  return (
    <div className="fixed inset-0 bg-black flex items-center justify-center z-50">
      <div className="bg-gray-900 rounded-xl p-6 w-full max-w-lg mx-4 border border-gray-700">
        <div className="flex justify-between items-center mb-6">
          <h2 className="text-xl font-medium text-white">Edit Central Knowledge Base</h2>
          <button
            onClick={onClose}
            className="text-gray-400 hover:text-white transition-colors"
          >
            <X size={24} />
          </button>
        </div>

        <form onSubmit={handleSubmit} className="space-y-6">
          <div>
            <label htmlFor="name" className="block text-sm font-medium text-gray-300 mb-2">
              Knowledge Base Name *
            </label>
            <input
              type="text"
              id="name"
              value={name}
              onChange={(e) => setName(e.target.value)}
              className="w-full px-3 py-2 bg-gray-800 border border-gray-600 rounded-lg text-white placeholder-gray-500 focus:border-purple-600 focus:outline-none"
              placeholder="Enter knowledge base name"
              required
            />
          </div>

          <div>
            <label className="block text-sm font-medium text-gray-300 mb-3">
              Preview Logo
            </label>
            
            {/* Preview Logo Display */}
            <div className="flex items-center gap-3 mb-4">
              <div className="w-12 h-12 bg-gray-800 border border-gray-600 rounded-lg flex items-center justify-center text-2xl">
                {selectedEmoji}
              </div>
            </div>

            {/* Search Bar */}
            <div className="relative mb-4">
              <Search className="absolute left-3 top-1/2 transform -translate-y-1/2 text-gray-400" size={16} />
              <input
                type="text"
                placeholder="🔍 Search emojis..."
                value={searchQuery}
                onChange={(e) => setSearchQuery(e.target.value)}
                className="w-full pl-10 pr-3 py-2 bg-gray-800 border border-gray-600 rounded-lg text-white text-sm placeholder-gray-500 focus:border-purple-600 focus:outline-none"
              />
            </div>

            {/* Unified Emoji Container */}
            <div className="bg-gray-800/30 border border-gray-600/50 rounded-xl overflow-hidden">
              {/* Emoji Grid */}
              <div className="p-4 max-h-48 overflow-y-auto">
                <div className="grid grid-cols-6 gap-2">
                  {filteredEmojis.map((emoji, index) => (
                    <button
                      key={`${emoji}-${index}`}
                      type="button"
                      onClick={() => handleEmojiSelect(emoji)}
                      className={`w-10 h-10 flex items-center justify-center text-lg rounded-lg transition-colors ${
                        selectedEmoji === emoji 
                          ? 'bg-purple-600 text-white' 
                          : 'hover:bg-gray-700/50'
                      }`}
                    >
                      {emoji}
                    </button>
                  ))}
                </div>
                {filteredEmojis.length === 0 && (
                  <div className="text-center text-gray-400 py-8 text-sm">
                    No emojis found
                  </div>
                )}
              </div>

              {/* Category Bar */}
              {!searchQuery && (
                <div className="border-t border-gray-600/30 bg-gray-800/20 px-4 py-3">
                  <div className="flex items-center gap-1 overflow-x-auto">
                    {Object.entries(categoryIcons).map(([category, icon]) => (
                      <button
                        key={category}
                        type="button"
                        onClick={() => setActiveCategory(category)}
                        className={`flex-shrink-0 w-8 h-8 flex items-center justify-center rounded-lg transition-colors ${
                          activeCategory === category
                            ? 'bg-purple-600 text-white'
                            : 'text-gray-400 hover:bg-gray-700/50 hover:text-white'
                        }`}
                        title={category}
                      >
                        <span className="text-sm">{icon}</span>
                      </button>
                    ))}
                  </div>
                </div>
              )}
            </div>
          </div>

          <div className="flex gap-3 pt-4">
            <button
              type="button"
              onClick={onClose}
              className="flex-1 px-4 py-2 border border-gray-600 text-gray-300 hover:bg-gray-700 rounded-lg transition-colors"
            >
              Cancel
            </button>
            <button
              type="submit"
              className="flex-1 px-4 py-2 bg-purple-600 hover:bg-purple-700 text-white font-medium rounded-lg transition-colors"
            >
              Save Changes
            </button>
          </div>
        </form>
      </div>
    </div>
  );
};

export default EditCentralKBModal; 